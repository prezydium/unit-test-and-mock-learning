package com.infoshareacademy.jjdd3.config;

public class DatabaseConfig {
    private String host;
    private int port;

    public DatabaseConfig() {
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }
}
